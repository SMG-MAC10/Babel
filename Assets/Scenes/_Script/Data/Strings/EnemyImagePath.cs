    public static string GetMonsterImageByElement(int element, string monster)
    {
        /// <summary>
        /// Type : int<br>
        /// 현재 필드의 속성<br>
        /// 0 = 토 (Soil)<br>
        /// 1 = 수 (Water)<br>
        /// 2 = 화 (Fire)<br>
        /// 3 = 목 (Wood)<br>
        /// 4 = 금 (Steal)</br></br></br></br></br></br>
        /// </summary>

        if (element == -1 | monster == "")
        {
            return null;
        }
        //Normal Monster Switch
        switch (monster)
        {
            case "Circle":
                switch (element)
                {
                    case 0:
                        return CircleSoil;
                    case 1:
                        return CircleWater;
                    case 2:
                        return CircleFire;
                    case 3:
                        return CircleWood;
                    case 4:
                        return CircleSteal;
                }
                break;
            case "Square":
                switch (element)
                {
                    case 0:
                        return SquareSoil;
                    case 1:
                        return SquareWater;
                    case 2:
                        return SquareFire;
                    case 3:
                        return SquareWood;
                    case 4:
                        return SquareSteal;
                }
                break;
            case "Triangle":
                switch (element)
                {
                    case 0:
                        return TriangleSoil;
                    case 1:
                        return TriangleWater;
                    case 2:
                        return TriangleFire;
                    case 3:
                        return TriangleWood;
                    case 4:
                        return TriangleSteal;
                }
                break;
            case "Semiboss":
                switch (element)
                {
                    case 0:
                        return SemibossSoil91;
                    case 1:
                        return SemibossWater91;
                    case 2:
                        return SemibossFire91;
                    case 3:
                        return SemibossWood91;
                    case 4:
                        return SemibossSteal91;
                }
                break;
            case "Boss":
                switch (element)
                {
                    case 0:
                        return BossSoil81;
                    case 1:
                        return BossWater81;
                    case 2:
                        return BossFire81;
                    case 3:
                        return BossWood81;
                    case 4:
                        return BossSteal81;
                }
                break;

            case "SemibossPawn":
                switch (element)
                {
                    case 0:
                        return SemibossPawn1Soil;
                    case 1:
                        return SemibossPawn1Water;
                    case 2:
                        return SemibossPawn1Fire;
                    case 3:
                        return SemibossPawn1Wood;
                    case 4:
                        return SemibossPawn1Steal;
                }
                break;
            case "BossPawn":
                switch (element)
                {
                    case 0:
                        return BossPawn1Soil;
                    case 1:
                        return BossPawn1Water;
                    case 2:
                        return BossPawn1Fire;
                    case 3:
                        return BossPawn1Wood;
                    case 4:
                        return BossPawn1Steal;
                }
                break;
        }
        return "";
    }

    public static string GetMonsterImageByFloor(int element, int floor, string monster)
    {
        /// <summary>
        /// Type : int<br>
        /// 현재 필드의 속성<br>
        /// 0 = 토 (Soil)<br>
        /// 1 = 수 (Water)<br>
        /// 2 = 화 (Fire)<br>
        /// 3 = 목 (Wood)<br>
        /// 4 = 금 (Steal)</br></br></br></br></br></br>
        /// </summary>
        /// 
        //battle/Enemies/BossMain/Fire/81/idle/01
        //battle/Enemies/BossMid/Ground/11/idle/01
        string bossType = "";
        string elm = "";
        switch (monster)
        {
            case "Boss":
                bossType = "BossMain";
                break;
            case "Semiboss":
                bossType = "BossMid";
                break;
        }

        switch (element)
        {
            case 0:
                elm = "Ground";
                break;
            case 1:
                elm = "Water";
                break;
            case 2:
                elm = "Fire";
                break;
            case 3:
                elm = "Grass";
                break;
            case 4:
                elm = "Gold";
                break;
        }
        return "battle/Enemies/" + bossType + "/" + elm + "/" + floor + "/idle/01";
    }
